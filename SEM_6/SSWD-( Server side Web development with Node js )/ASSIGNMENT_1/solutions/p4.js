import { myarea, mycircumference } from "./circle.js";

const radius = 5;

console.log("Area of circle with radius " + radius + " is: " + myarea(radius));
console.log(
  "Circumference of circle with radius " +
    radius +
    " is: " +
    mycircumference(radius)
);

/*
Area of circle with radius 5 is: 78.53981633974483
Circumference of circle with radius 5 is: 31.41592653589793
*/
